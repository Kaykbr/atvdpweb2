let resposta = prompt("Deseja continuar? (sim ou nao)");

if (resposta === "sim") {
    alert("Você escolheu continuar. Boa escolha!");
} else if (resposta === "nao") {
    alert("Você escolheu não continuar. Ateh a proxima!");
} else {
    alert("Resposta inválida. Por favor, digite 'sim' ou 'nao'.");
}